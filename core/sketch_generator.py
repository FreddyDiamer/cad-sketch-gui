"""Формирование эскиза по найденным контурам.

Эскиз представлен словарём с двумя полями:
    * entities — список геометрических примитивов (Polyline и Circle);
    * scale    — масштабный коэффициент мм/пиксель, сохранённый
                 на момент построения эскиза.

Координаты примитивов оставляются в пикселях изображения. Это
позволяет корректно отображать эскиз поверх растра в WorkspaceView.
Перевод в миллиметры выполняется только в момент экспорта в DXF
(см. core/file_operations.py).
"""
from __future__ import annotations

from core.models import Calibration, Circle, Polyline


class SketchGenerator:
    """Сборщик эскиза: получает список контуров и формирует эскиз.

    Логика намеренно простая — обработка изображения (Canny, морфология,
    аппроксимация Дугласа–Пёкера) уже выполнена в ContourDetector.
    Этот класс лишь упаковывает результат и сохраняет масштаб.
    """

    def build(
        self,
        contours: list[Polyline | Circle],
        calibration: Calibration | None = None,
    ) -> dict:
        """Создаёт эскиз из списка примитивов.

        Параметр `calibration` нужен только для запоминания текущего
        масштаба. Сами координаты остаются в пикселях; масштабирование
        откладывается до экспорта в DXF.
        """
        scale = calibration.scale_mm_per_pixel if calibration is not None else 1.0
        # Защита от нулевого/отрицательного масштаба (например, если
        # пользователь отменил калибровку или указал нулевое расстояние).
        if scale <= 0:
            scale = 1.0
        return {"entities": list(contours), "scale": scale}
